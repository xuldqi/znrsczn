export const ArticleProvider = ({ children }) => {
  const [articles, setArticles] = useState(initialArticles)

  // 从localStorage读取数据
  useEffect(() => {
    const savedArticles = localStorage.getItem('articles')
    if (savedArticles) {
      try {
        const parsedArticles = JSON.parse(savedArticles)
        setArticles(parsedArticles)
      } catch (error) {
        console.error('Error parsing saved articles:', error)
        setArticles(initialArticles)
      }
    }
  }, [])

  // 保存数据到localStorage
  useEffect(() => {
    localStorage.setItem('articles', JSON.stringify(articles))
  }, [articles])

  // 获取单篇文章
  const getArticle = (id) => {
    return articles[id] || null
  }

  // 获取所有文章
  const getAllArticles = () => {
    return Object.values(articles)
  }

  // 按分类获取文章
  const getArticlesByCategory = (category) => {
    return Object.values(articles).filter(article => 
      article.category === category && article.status === 'published'
    )
  }

  // 获取已发布的文章
  const getPublishedArticles = () => {
    return Object.values(articles).filter(article => article.status === 'published')
  }

  // 添加新文章
  const addArticle = (articleData) => {
    const id = generateSlug(articleData.title)
    const newArticle = {
      id,
      ...articleData,
      date: new Date().toISOString().split('T')[0],
      status: 'draft'
    }
    
    setArticles(prev => ({
      ...prev,
      [id]: newArticle
    }))
    
    return id
  }

  // 更新文章
  const updateArticle = (id, updates) => {
    setArticles(prev => ({
      ...prev,
      [id]: {
        ...prev[id],
        ...updates
      }
    }))
  }

  // 删除文章
  const deleteArticle = (id) => {
    setArticles(prev => {
      const newArticles = { ...prev }
      delete newArticles[id]
      return newArticles
    })
  }

  // 生成文章slug
  const generateSlug = (title) => {
    return title
      .toLowerCase()
      .replace(/[^\w\s-]/g, '')
      .replace(/\s+/g, '-')
      .substring(0, 50) + '-' + Date.now()
  }

  const contextValue = {
    articles,
    getArticle,
    getAllArticles,
    getArticlesByCategory,
    getPublishedArticles,
    addArticle,
    updateArticle,
    deleteArticle
  }

  return (
    <ArticleContext.Provider value={contextValue}>
      {children}
    </ArticleContext.Provider>
  )
}

// 使用文章上下文的Hook
export const useArticles = () => {
  const context = useContext(ArticleContext)
  if (!context) {
    throw new Error('useArticles must be used within an ArticleProvider')
  }
  return context
}

export default ArticleContext